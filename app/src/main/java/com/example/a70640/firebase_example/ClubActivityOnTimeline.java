public class ClubActivityOnTimeline {
    int id;
    String clubName;
    String activityName;
    String thumbnail;
    String time;
    String location;

    public ClubActivityOnTimeline() {
    }

    public int getId() {
        return id;
    }

    public String getClubName() {
        return clubName;
    }

    public String getActivityName() {
        return activityName;
    }

    public String getThumbnail() {
        return thumbnail;
    }

    public String getTime() {
        return time;
    }

    public String getLocation() {
        return location;
    }
}
